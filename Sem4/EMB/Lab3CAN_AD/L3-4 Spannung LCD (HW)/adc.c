
#include "lpc17xx.h"                              /* LPC17xx definitions    */
#include "adc.h"

/*----------------------------------------------------------------------------
  config ADC
 *----------------------------------------------------------------------------*/
void ADC_Config (void) {

  LPC_SC->PCONP |= (1<<12);
	LPC_PINCON->PINSEL3 |= (1<<31)|(1<<30);
	LPC_ADC->ADCR |= (1<<21)|(1<<8)|(1<<5);
	LPC_ADC->ADCR &= ~(1<<0);
}

/*----------------------------------------------------------------------------
  start ADC Conversion
 *----------------------------------------------------------------------------*/
void ADC_StartConversion (void) {
  LPC_ADC->ADCR |= (1<<24);
}

